    public Snapshot criarSnapshot() {
        return new Snapshot(
            this, 
            campoNome.getText(), 
            campoIdade.getText(), 
            campoCargo.getText(), 
            campoSalario.getText()
        );
    }
    
    class Snapshot {
        private Formulario formulario;
        private String nome, idade, cargo, salario;

        public Snapshot(
            Formulario formulario, String nome, String idade, 
            String cargo, String salario
        ) {
            this.formulario = formulario;
            this.nome = nome;
            this.idade = idade;
            this.cargo = cargo;
            this.salario = salario;
        }
        
        public void restaurar() {
            formulario.campoNome.setText(nome);
            formulario.campoIdade.setText(idade);
            formulario.campoCargo.setText(cargo);
            formulario.campoSalario.setText(salario);
        }
    }

